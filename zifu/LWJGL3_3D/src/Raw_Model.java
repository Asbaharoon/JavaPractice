public class Raw_Model
{
  private int vaoid;
  private int vertexCount;
  
  public Raw_Model(int vaoid, int vertexCount)
  {
    this.vaoid = vaoid;
    this.vertexCount = vertexCount;
  }
  
  public int getVaoid()
  {
    return this.vaoid;
  }
  
  public int getVertexCount()
  {
    return this.vertexCount;
  }
}
